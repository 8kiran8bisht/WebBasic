/* About Us information div*/

function showAboutUs() {
    var dynamicDiv = document.querySelector("#dynamicDiv");
    dynamicDiv.innerHTML = "";
    dynamicDiv.classList.remove("hidden");
    var Dy_single_box = document.createElement("div");
    var pageTitle = document.createElement("h1");
    pageTitle.innerHTML = "About Us";
    dynamicDiv.appendChild(pageTitle);
    Dy_single_box.classList.add("aboutUs");
    var abtUs = document.createElement("p");
    abtUs.innerHTML =
        "WishTree is an Ontario based online shopping company. Our products are locally produced in partnership with the craftsmen community in the Greater Toronto Area. Through our outlet the craftsmen can showcase their skills and customers can shop for all Christmas related products. We specialize in Christmas decoration material, Christmas Trees, colorful Christmas Lights, and delicious Christmas Chocolates. Customer delight is our motto, and we assure speedy delivery to all provinces in Canada.";
    Dy_single_box.appendChild(abtUs);
    var add = document.createElement("span");

    add.innerHTML =
        "WishTree " +
        "<br>" +
        "ABC Avenue E," +
        "<br>" +
        "North York, ON MmM nNn" +
        "<br>" +
        "+1 (111) 111-1111" +
        "<br>" +
        "abc@whishtree.com";

    Dy_single_box.appendChild(add);

    dynamicDiv.appendChild(Dy_single_box);
}
/* Dynamic div for sub-categories*/

function show(value) {
    var dynamicDiv = document.querySelector("#dynamicDiv");
    dynamicDiv.innerHTML = "";
    dynamicDiv.classList.remove("hidden");

    for (var i = 0; i < value.length; i++) {
        var Dy_single_box = document.createElement("div");
        Dy_single_box.classList.add("d-single-box");

        var Dy_img_area = document.createElement("div");
        Dy_img_area.classList.add("d-img-area");

        var Dimg = document.createElement("img");
        Dimg.src = value[i].url;
        Dimg.width = 180;

        var p = document.createElement("span");
        p.innerHTML = value[i].description;

        var price = document.createElement("p");
        price.innerHTML = "<strong>" + value[i].price + "</strong>";

        var submit = document.createElement("button");
        submit.innerHTML = "Buy Now";
        submit.id = "button";

        submit.height = 10;
        Dy_img_area.appendChild(Dimg);

        Dy_single_box.appendChild(Dy_img_area);
        Dy_single_box.appendChild(p);
        Dy_single_box.appendChild(price);
        Dy_single_box.appendChild(submit);

        dynamicDiv.appendChild(Dy_single_box);
    }
}
